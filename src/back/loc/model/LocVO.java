package back.loc.model;

public class LocVO implements java.io.Serializable{
	private String loc_no;
	private String loc_desc;
	
	public String getLoc_no() {
		return loc_no;
	}
	public void setLoc_no(String loc_no) {
		this.loc_no = loc_no;
	}
	public String getLoc_desc() {
		return loc_desc;
	}
	public void setLoc_desc(String loc_desc) {
		this.loc_desc = loc_desc;
	}
	
	

}